export const getTranslation = (lang, key) => {
  const keys = key.split('.');
  let value = translations[lang] || translations.fr;
  for (const k of keys) {
    value = value?.[k];
    if (!value) return key;
  }
  return value || key;
};

export const getAvailableLanguages = () => {
  return Object.keys(translations);
};

